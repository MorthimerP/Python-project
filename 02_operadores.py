# Operaciones aritmeticos
print(10 + 2)
print(10 - 2)
print(10 * 2)
print(10 / 2)   #Devuelve un float
print(10 % 3)
print(10 // 3)  #Devuelve un int redondeado hacia abajo 
print(2 ** 3)
print(2 + 4 ** 3 / 2 // 3 -1)

#+ Operaciones con cadenas
print("Hola " + "Python ")
print("Hola " + str(10))
print("Python " * 3)
print("Palabra " * (2 ** 3))

var_float = 2.5 * 2
print("Operador " * int(var_float))

#? Operadores comparativos
print(2 < 4)
print(2 > 4)
print(2 <= 4)
print(2 >= 4)
print(3 == 4)
print(3 != 4)

print("\n")
#& Comparaciones de strings
print("Hola" < "Python")
print("aaaa" > "ba")
print("Hola" <= "Python")
print("z" >= "a")
print("Hola" == "Python")
print("Hola" != "Python")

print("\n")
#* Operadores logicos
print(4 < 10 and "a" > "b")
print(4 < 10 or "a" > "b")
print(not(2 < 3))